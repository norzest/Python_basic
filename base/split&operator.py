a, b = input("실수 두 개 입력>>").split()
a, b = float(a), float(b)
print(f"{a} + {b} = {a+b}")
print(f"{a} - {b} = {a-b}")
print(f"{a} * {b} = {a*b}")
print(f"{a} / {b} = {round(a/b, 3)}")
print(f"{a} // {b} = {a//b}")
print(f"{a} % {b} = {a%b}")
print(f"{a} ** {b} = {a**b}")
print(f"{a} ** 0.5 = {a**0.5}")
print(f"{b} ** 0.5 = {b**0.5}")